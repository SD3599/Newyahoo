package com.hellokoding.auth.model;

public class WeatherCondition {
	private String Text;
	private int Code;
	private String Temp;
	public String getText() {
		return Text;
	}
	public void setText(String text) {
		Text = text;
	}
	public int getCode() {
		return Code;
	}
	public void setCode(int code) {
		Code = code;
	}
	public String getTemp() {
		return Temp;
	}
	public void setTemp(String temp) {
		Temp = temp;
	}
	

}
